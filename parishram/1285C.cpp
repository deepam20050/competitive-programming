using lli = long long;
using pii = pair < int, int >;

void test_case() {
  lli n; cin >> n;
  int ans;
  for (int i = 1; i * 1ll * i <= n; ++i) {
    if (n % i == 0 && lcm(i * 1ll, n / i) == n) {
      ans = i;
    }
  }
  cout << ans << " " << n / ans << '\n';
}

