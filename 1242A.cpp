void test_case() {
  lli n; cin >> n;
  lli ans = n;
  for (lli i = 2; i * i <= n; ++i) {
    if (n % i == 0) {
      ans = gcd(ans, gcd(i, n / i));
    }
  }
  cout << ans << '\n';
}

